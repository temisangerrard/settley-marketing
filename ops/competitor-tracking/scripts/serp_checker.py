def check_competitor_site(url: str) -> Dict:
    """
    Check competitor website for changes.
    Uses simple HTTP HEAD request to check last-modified.
    """
    try:
        response = requests.head(f"https://{url}", timeout=10, allow_redirects=True)
        return {
            "url": url,
            "status_code": response.status_code,
            "last_modified": response.headers.get("last-modified", "N/A"),
            "content_length": response.headers.get("content-length", "N/A"),
        }
    except Exception as e:
        return {
            "url": url,
            "status_code": "ERROR",
            "error": str(e),
        }


